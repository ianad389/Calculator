function add(a, b) {
    return a + b;
  }
  
  console.log(add(1, 2)); // 3
  
  function subtract(a, b) {
    return a - b;
  }
  
  console.log(subtract(10, 5)); // 5
  
  function multiply(a, b) {
    return a * b;
  }
  
  console.log(multiply(2, 3)); // 6
  
  function divide(a, b) {
    return a / b;
  }
  
  console.log(divide(12, 6)); // 2